    private void showData(List<SinhVien> list) {
        dtm.setRowCount(0);
        for (SinhVien o : list) {
            dtm.addRow(new Object[]{
                o.getId(),
                o.getPhong(),
                o.getToan(),
                o.getLy(),
                o.getHoa(),
                o.getDiemTB()
            });
        }
    }
//vinh đầu đất
    private void loadCbo() {
        cboPhong.removeAllItems();
        for (int i = 301; i <= 310; i++) {
            cboPhong.addItem("P" + i);
        }
    }

    private SinhVien getToForm() {
        String ten = txtTen.getText();
        String phong = (String) cboPhong.getSelectedItem();
        String toan = txtToan.getText();
        String ly = txtLy.getText();
        String hoa = txtHoa.getText();
        if (ten.isBlank() || toan.isBlank() || ly.isBlank() || hoa.isBlank()) {
            return null;
        } else {
            try {
                Double t = Double.parseDouble(toan);
                Double l = Double.parseDouble(ly);
                Double h = Double.parseDouble(hoa);
            } catch (Exception e) {
                return null;
            }
        }
        return new SinhVien(ten, phong, Double.parseDouble(toan),
                Double.parseDouble(ly), Double.parseDouble(hoa));
    }

    private void detail(SinhVien sv) {
        txtTen.setText(sv.getTenSV());
        txtToan.setText(sv.getToan() + "");
        txtLy.setText(sv.getLy() + "");
        txtHoa.setText(sv.getHoa() + "");
        cboPhong.setSelectedItem(sv.getPhong() + "");
    }

